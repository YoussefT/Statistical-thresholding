function [ flambda ] = bumps_fun(x,lambda0)

Ibumps = 73355397868307820710362413437946978142421390622935203073917363/261818528963847906790624505138383771335405686604193279980000000;

xj = [0.1,0.13,0.15,0.23,0.25,0.4,0.44,0.65,0.76,0.78,0.81];
hj = [4,5,3,4,5,4.2,2.1,4.3,3.1,5.1,4.2];
wj = [0.005,0.005,0.006,0.01,0.01,0.03,0.01,0.01,0.005,0.008,0.005];

X = repmat(x,11,1);
Xj = repmat(xj',1,length(x));
Hj = repmat(hj',1,length(x));
Wj = repmat(wj',1,length(x));

flambda(:,1) = sum(Hj.*(1+abs(X-Xj)./Wj).^(-4),1)/Ibumps;
flambda = 0.25*lambda0*flambda + 1.75*lambda0;
            
end

